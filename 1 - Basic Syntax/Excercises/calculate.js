function calculation(numOne, numTwo) {
    let sum = numOne + numTwo;
    let subtract = numOne - numTwo;
    let multiply = numOne * numTwo;
    let average = (numOne + numTwo) / 2;

    console.log('Sum: ' + sum);
    console.log('Difference: ' + subtract);
    console.log('Product: ' + multiply);
    console.log('Average: ' + average);
}

