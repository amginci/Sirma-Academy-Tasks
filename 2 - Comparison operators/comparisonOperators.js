// Equality operators
let a = 5;
console.log(a == 5);
console.log(a == 6);

// Innequallity operator
let b = 10;
console.log(a != b);
console.log(a != 5);

// Greater than and less than
console.log(a < b);
console.log(a + 6 > b);

// Greater/less than or equa
console.log(a <= 5);
console.log(a <= 6);
console.log(b >= a);

// Strict equality (idemtity)
console.log(5 == '5'); // true, allows coercion
console.log(5 === '5'); // false, doesn't allow coercion

// Strict inequality
console.log(5 !== '5'); //false
console.log(5 != '5'); //true

